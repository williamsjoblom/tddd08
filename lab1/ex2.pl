/****************************************************************
 * path/2
 ****************************************************************/

/* Trivial case */
path(X, X).

path(X, Z) :-
    edge(X, Y),
    path(Y, Z).

/****************************************************************
 * path/3
 ****************************************************************/

/* Trivial case */
path(X, X, [X]).

path(X, Z, [X|Rest]) :-
    edge(X, Y),
    path(Y, Z, Rest).

/****************************************************************
 * npath/2
 ****************************************************************/

npath(X, Y, N) :-
    path(X, Y, L),
    length(L, N),
    write('Path: '), write(L),
    nl.
